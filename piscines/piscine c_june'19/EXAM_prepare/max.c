int		max(int *tab, unsigned int len)
{
	int i;
	int max;

	i = 0;
	max = tab[0];
	if(tab == '\0')
		return (0);
	while (len-- != 0)
	{
		if (tab[i] > max)
		{
			max = tab[i];
		}
		i++;
	}
	return (max);
}
