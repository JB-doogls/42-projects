#include <unistd.h>

int		main(int argc, char **argv)
{
	int ct;

	ct = 0;
	if (argc != 2)
		write(1, "a\n", 2);
	else
	{
		while(argv[1][ct])
		{
			if (argv[1][ct] == 'a')
			{
				write(1, "a", 1);
				break;
			}
			ct++;
		}
		write(1, "\n", 1);
	}
	return (0);
}
