#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if ((long)n <= -2147483648)
		ft_putstr_fd("-2147483648", fd);
	else if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = (unsigned int)(n * -1);
	}
	else if (n >= 10)
	{
		n = (unsigned int)n;
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
		ft_putchar_fd((char)(n + '0'), fd);
}
