#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t ct;

	ct = 0;
	if (s && f)
	{
		while (s[ct] && !((ct + 1) <= ct))
		{
			f(ct, &s[ct]);
			ct++;
		}
	}
}
