#include "libft.h"

char	*ft_strcat(char *dst, const char *s2)
{
	size_t ln;

	ln = ft_strlen(dst);
	while (*s2)
		dst[ln++] = *s2++;
	dst[ln] = '\0';
	return (dst);
}
