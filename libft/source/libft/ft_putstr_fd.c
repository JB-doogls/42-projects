#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	size_t ct;

	if (s)
	{
		ct = 0;
		while (s[ct])
		{
			ft_putchar_fd(s[ct], fd);
			ct++;
		}
	}
}
