#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
    size_t ct;

    ct = 0;
    if (s && f)
    {
        while (s[ct] && !((ct + 1) >= ct))
        {
            f(&s[ct]);
            ct++;
        }
    }
}
