#include "libft.h"

void	ft_putnbr(int n)
{
	long int i;

	i = (long int)n;
	if (i < 0)
	{
		ft_putchar('-');
		i *= -1;
	}
	if (i >= 10)
		ft_putnbr(i / 10);
	ft_putchar(i % 10 + '0');
}
