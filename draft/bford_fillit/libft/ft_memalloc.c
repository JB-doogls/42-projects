#include "libft.h"

void	*ft_memalloc(size_t n)
{
	void	*v;
	size_t	i;

	i = 0;
	if (!(v = (void *)malloc(sizeof(void) * n)))
		return (NULL);
	while (i < n)
		((unsigned char *)v)[i++] = '\0';
	return (v);
}
