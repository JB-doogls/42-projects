#include "libft.h"

void	*ft_memmove(void *s1, const void *s2, size_t n)
{
	size_t i;

	i = 0;
	if (s1 < s2)
		while (i < n)
		{
			((unsigned char *)s1)[i] = ((unsigned char *)s2)[i];
			i++;
		}
	else
		ft_memcpy(s1, s2, n);
	return (s1);
}
