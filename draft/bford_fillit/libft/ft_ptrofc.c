#include <string.h>

char	*ft_ptrofc(char *s, int c, int n)
{
	int	i;

	i = 0;
	while (*s)
	{
		if (*s == c)
			i++;
		if (i == n)
			return (s);
		s++;
	}
	return (NULL);
}
