#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	char *ptr;

	ptr = s1;
	while (n && *s2)
	{
		*s1++ = *s2++;
		n--;
	}
	while (n)
	{
		*s1++ = '\0';
		n--;
	}
	return (ptr);
}
